let print_cmd =
  Command.basic
    ~summary:"print hello world"
    (let%map_open.Command () = return () in
     fun () -> Eio_main.run @@ fun env -> Eio_writer.print_sexp ~env Vcs.hello_world)
;;

let main = Command.group ~summary:"" [ "print", print_cmd ]
